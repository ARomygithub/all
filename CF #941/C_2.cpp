template<typename T>
void min_self(T& A, T B) {
    A = min(A,B);
}
template<typename T>
void max_self(T& A, T B) {
    A = max(A,B);
}

const int mxn=2e5;
int n,t;
string s;

void solve() {
    cin >>n;
    cin >>s;
    int cur = 0;
    int mn = 0, mx=0;
    for(int i=0;i<sz(s);i++) {
        if(((cur%2)==0) == (s[i]=='1')) {
            cur++;
        } else {
            cur--;
        }
        max_self(mx, cur);
        min_self(mn, cur);
    }
    cout <<mx - mn <<"\n";
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    cin >>t;
    while(t--) {
        solve();
    }
}