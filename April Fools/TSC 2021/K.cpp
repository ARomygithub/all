template<typename T>
void min_self(T& A, T B) {
    A = min(A,B);
}
template<typename T>
void max_self(T& A, T B) {
    A = max(A,B);
}

const int mxn=1e5;
string s;

void solve() {
    cin >>s;
    int ans = 1;
    for(int i=0;i<sz(s);) {
        int j = i;
        while(j<sz(s) && s[j]==s[i]) {
            j++;
        }
        ans += min(j-i,2);
        i = j;
    }
    cout <<ans <<"\n";
}

