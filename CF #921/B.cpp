template<typename T>
void min_self(T& A, T B) {
    A = min(A,B);
}
template<typename T>
void max_self(T& A, T B) {
    A = max(A,B);
}

const int mxn=1e5;
int n,x,t;

void solve() {
    cin >>x >>n;
    int ans = 1;
    for(int i=2;i*i<=x;i++) {
        if((x%i)==0) {
            if(i>=n) {
                max_self(ans, x/i);
            }
            if((x/i)>=n) {
                max_self(ans, i);
            }
        }
    }
    if(n==1) {
        max_self(ans, x);
    }
    cout <<ans <<"\n";
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    cin >>t;
    while(t--) {
        solve();
    }
}