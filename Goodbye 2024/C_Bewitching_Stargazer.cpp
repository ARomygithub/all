const int mxn=1e5;
int t;
ll n,k;

ll getans(ll len, ll ct, ll ad) {
    ll x = (len-1)/2;
    ll ret = 0;
    if(len%2) {
        ret += x*ct+ad;
    }
    if(len/2 < k) return ret;
    ret += getans(len/2, ct*2, ad*2 + (x+1)*ct);
    return ret;
}

void solve() {
    cin >>n >>k;
    cout <<getans(n,1,1) <<"\n";
}

