template<typename T>
void min_self(T& A, T B) {
    A = min(A,B);
}
template<typename T>
void max_self(T& A, T B) {
    A = max(A,B);
}

const int mxn=1e5;
int n,d,k,t;

void solve() {
    cin >>n >>d >>k;
    d %=n;
    int gc = __gcd(n,d);
    int a = (k-1)%(n/gc); int b = (k-1)/(n/gc);
    int ans = (1ll*a*d)%(1ll*n);
    ans = (ans+b)%n;
    cout <<ans <<"\n";
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    cin >>t;
    while(t--) {
        solve();
    }
}