const int mxn=1e5;
ll r;

void solve() {
    cin >>r;
    ll ans = 0;
    for(ll xx=-(r*2-1);xx+2<=r*2;xx+=2) {
        // debug() <<imie(xx) imie(r*2);
        ll xxi = (xx<0?xx:xx+2);
        ll cur = (r*2)*(r*2) - xxi*xxi;
        // debug() <<imie(cur);
        ll yy = 0;
        ll li = 0, ri = r*2-1;
        while(li<=ri) {
            ll mid = (li+ri)/2;
            if(mid*mid<=cur) {
                yy = mid;
                li = mid+1;
            } else {
                ri = mid-1;
            }
        }
        // debug() <<imie(yy);
        if(yy>=1) {
            ans++;
            if((yy%2)==0) yy--;
            yy--;
            ans += yy;
        }
        // debug() <<imie(ans);
    }
    cout <<ans <<"\n";
}

