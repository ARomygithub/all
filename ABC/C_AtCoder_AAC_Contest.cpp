template<typename T>
void min_self(T& A, T B) {
    A = min(A,B);
}
template<typename T>
void max_self(T& A, T B) {
    A = max(A,B);
}

const int mxn=1e5,mxa=1e9;
ll a,b,c,t;

void solve() {
    cin >>a >>b >>c;
    ll ans = 0;
    ll l = 1, r = mxa;
    while(l<=r) {
        ll mid = (l+r)/2;
        ll ct = mid*3;
        if(ct<=(a+b+c) && mid<=min(a,c)) {
            ans = mid;
            l = mid+1;
        } else {
            r = mid-1;
        }
    }
    cout <<ans <<"\n";
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);
    cin >>t;
    while(t--) {
        solve();
    }
}