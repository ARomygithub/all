template<typename T>
void min_self(T& A, T B) {
    A = min(A,B);
}
template<typename T>
void max_self(T& A, T B) {
    A = max(A,B);
}

const int mxn=1e5;
ll n;

void solve() {
    cin >>n;
    n--;
    string ans="";
    if(n==0) {
        ans +='0';
    }
    while(n>0) {
        int temp = (n%5)*2;
        ans +=(char)('0'+temp);
        n /=5;
    }
    reverse(ans.begin(),ans.end());
    cout <<ans <<"\n";
}

