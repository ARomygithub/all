template<typename T>
void min_self(T& A, T B) {
    A = min(A,B);
}
template<typename T>
void max_self(T& A, T B) {
    A = max(A,B);
}

const int mxn=1e5;
ll n;

void solve() {
    cin >>n;
    ll ans=0;
    for(ll i=1;i<=n;i++) {
        ll temp=0;
        ll u = i;
        while(u) {
            temp +=(u%10);
            u /=10;
        }
        if((i%temp)==0) {
            ans++;
        }
    }
    cout <<ans <<"\n";
}

